#ifndef H_KARTA
#define H_KARTA

/*!
Klasa reprezentuje pojedyncza karte zawiera pola wartosc oraz kolor
*/
class KARTA
{
	//____________________________________ POLA ___________________________________________________
	
	int wartosc;
	int kolor;

	//_____________________________________________________________________________________________

public:
	
	/*!
	Konstruktor bezargumentowy klasy KARTA
	*/
	KARTA();
	
	/*!
	Akcesor do pola wartosc karty
	*/
	int a_war();
	
	/*!
	Akcesor do pola kolor karty
	*/
	int a_kol();
	
	/*!
	Konstruktor argumentowy klasy KARTA
	*/
	KARTA(int wartosc1, int kolor1);
	
	/*!
	Destruktor klasy KARTA
	*/
	~KARTA();
};

#endif